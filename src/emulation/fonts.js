const sliceFont = function (fontData, char, size) {
  const index = parseInt(char, 16) * size
  const font = fontData.slice(index, index + size);

  var string = ''

  font.forEach(char => {
    var line = ''
    for (let i = 256; i >= 1; i /= 2) {
      line += char & i ? 'x' : ' '
    }
    string += `${line}\n`
  });

  return string
}

const printFont = function (char) {
  return sliceFont(normalFont, char, 5)
}

const printExtendedFont = function (char) {
  return sliceFont(extendedFont, char, 9)
}

export { normalFont, extendedFont, printFont, printExtendedFont }
